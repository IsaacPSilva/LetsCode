#6. Faça um programa que converta um valor em metros para centímetros.

print('Convertendo de metros para centímetros')
print('-'*40)

#Informando valor de metros na entrada
met = int(input('Informe um valor: '))

#Convertendo para centimetros
cen = met * 100

print('-'*40)
print(f'O valor convertido de {met} metros para centimetros é {cen}')